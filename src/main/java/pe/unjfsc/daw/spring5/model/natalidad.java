package pe.unjfsc.daw.spring5.model;

public class natalidad {
	
	private int cuiaNata;
	private int idSexo;
	private int idGeno;
	private String fechNaciNata;
	private double pesoNata;
	private int cuiaMadr;
	private int cuiaPadr;
	private int codiProp;
	private String obse;
	private String foto;
	private int esta;

	public int getCuiaNata() {
		return cuiaNata;
	}
	public void setCuiaNata(int cuiaNata) {
		this.cuiaNata = cuiaNata;
	}
	public int getIdSexo() {
		return idSexo;
	}
	public void setIdSexo(int idSexo) {
		this.idSexo = idSexo;
	}
	public int getIdGeno() {
		return idGeno;
	}
	public void setIdGeno(int idGeno) {
		this.idGeno = idGeno;
	}
	public String getFechNaciNata() {
		return fechNaciNata;
	}
	public void setFechNaciNata(String fechNaciNata) {
		this.fechNaciNata = fechNaciNata;
	}
	public double getPesoNata() {
		return pesoNata;
	}
	public void setPesoNata(double pesoNata) {
		this.pesoNata = pesoNata;
	}
	public int getCuiaMadr() {
		return cuiaMadr;
	}
	public void setCuiaMadr(int cuiaMadr) {
		this.cuiaMadr = cuiaMadr;
	}
	public int getCuiaPadr() {
		return cuiaPadr;
	}
	public void setCuiaPadr(int cuiaPadr) {
		this.cuiaPadr = cuiaPadr;
	}
	public int getCodiProp() {
		return codiProp;
	}
	public void setCodiProp(int codiProp) {
		this.codiProp = codiProp;
	}
	public String getObse() {
		return obse;
	}
	public void setObse(String obse) {
		this.obse = obse;
	}
	public String getFoto() {
		return foto;
	}
	public void setFoto(String foto) {
		this.foto = foto;
	}
	public int getEsta() {
		return esta;
	}
	public void setEsta(int esta) {
		this.esta = esta;
	}
	
	
}
