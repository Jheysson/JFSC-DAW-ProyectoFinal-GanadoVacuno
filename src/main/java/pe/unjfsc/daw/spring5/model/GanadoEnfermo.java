package pe.unjfsc.daw.spring5.model;

public class GanadoEnfermo {
	
	private int cuiaGana;
	private int idEnfe;
	private String fechIngr;
	private String obse;
	private int esta;
	
	public int getCuiaGana() {
		return cuiaGana;
	}
	public void setCuiaGana(int cuiaGana) {
		this.cuiaGana = cuiaGana;
	}
	public int getIdEnfe() {
		return idEnfe;
	}
	public void setIdEnfe(int idEnfe) {
		this.idEnfe = idEnfe;
	}
	public String getFechIngr() {
		return fechIngr;
	}
	public void setFechIngr(String fechIngr) {
		this.fechIngr = fechIngr;
	}
	public String getObse() {
		return obse;
	}
	public void setObse(String obse) {
		this.obse = obse;
	}
	public int getEsta() {
		return esta;
	}
	public void setEsta(int esta) {
		this.esta = esta;
	}

	
}
