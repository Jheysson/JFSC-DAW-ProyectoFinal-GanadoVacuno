package pe.unjfsc.daw.spring5.model;

public class GanadoMuerto {
	private int idGanaMuer;
	private int cuiaGana;
	private String aliaGana;
	private String fechNaciGana;
	private int edadGana;
	private int idGeno;
	private String nombEtapa;
	private String causMuer;
	private int esta;
	public int getIdGanaMuer() {
		return idGanaMuer;
	}
	public void setIdGanaMuer(int idGanaMuer) {
		this.idGanaMuer = idGanaMuer;
	}
	public int getCuiaGana() {
		return cuiaGana;
	}
	public void setCuiaGana(int cuiaGana) {
		this.cuiaGana = cuiaGana;
	}
	public String getAliaGana() {
		return aliaGana;
	}
	public void setAliaGana(String aliaGana) {
		this.aliaGana = aliaGana;
	}
	public String getFechNaciGana() {
		return fechNaciGana;
	}
	public void setFechNaciGana(String fechNaciGana) {
		this.fechNaciGana = fechNaciGana;
	}
	public int getEdadGana() {
		return edadGana;
	}
	public void setEdadGana(int edadGana) {
		this.edadGana = edadGana;
	}
	public int getIdGeno() {
		return idGeno;
	}
	public void setIdGeno(int idGeno) {
		this.idGeno = idGeno;
	}
	public String getNombEtapa() {
		return nombEtapa;
	}
	public void setNombEtapa(String nombEtapa) {
		this.nombEtapa = nombEtapa;
	}
	public String getCausMuer() {
		return causMuer;
	}
	public void setCausMuer(String causMuer) {
		this.causMuer = causMuer;
	}
	public int getEsta() {
		return esta;
	}
	public void setEsta(int esta) {
		this.esta = esta;
	}
	
	
	
}
